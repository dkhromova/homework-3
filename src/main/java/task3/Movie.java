package task3;

public class Movie {
    String name;
    double rating;
    String genre;
    String country;
    boolean isOscar;
    int year;


    public Movie(String name, double rating, String genre, String country, boolean isOscar, int year) {
        this.name = name;
        this.rating = rating;
        this.genre = genre;
        this.country = country;
        this.isOscar = isOscar;
        this.year = year;
    }
}
